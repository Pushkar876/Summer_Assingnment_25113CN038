//Write a program to Print half pyramid pattern.
package Day8;


public class program29 {
    public static void main(String[] args) {
        int n = 5;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print("* ");
                
            }
            System.out.println();

        }
        
    }
}
